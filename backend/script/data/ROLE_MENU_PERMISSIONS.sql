DELETE FROM ROLE_MENU_PERMISSIONS; 
INSERT INTO ROLE_MENU_PERMISSIONS (ROLE_ID, MENU_ID, CAN_VIEW, CAN_EDIT)
SELECT 
    'admin' AS ROLE_ID, 
    MENU_ID, 
    'Y' AS CAN_VIEW, 
    'Y' AS CAN_EDIT
FROM 
    MENU
WHERE 
    NOT EXISTS (
        SELECT 1
        FROM ROLE_MENU_PERMISSIONS
        WHERE ROLE_MENU_PERMISSIONS.ROLE_ID = 'admin'
          AND ROLE_MENU_PERMISSIONS.MENU_ID = MENU.MENU_ID
    );

COMMIT;
